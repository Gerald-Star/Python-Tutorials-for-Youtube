
#? Slicing Strings

# Strings can be sliced using the slice operator [].

# Here you master the indexing of strings in Python.
# First index of element = 0
# Second index of element = 1
# Third index of element = 2 etc
# Last index of element = -1
# Second last index of element = -2

# The first element of a string can be accessed using the index 0.
our_string1 = "Continents"
print(our_string1[0])

print()

# The second element of a string can be accessed using the index 1.
our_string2 = "Continents"
print(our_string2[1])

print()

# The third element of a string can be accessed using the index 2.
our_string3 = "Continents"
print(our_string3[2])


# The character from position 2 to position 5
our_string4 = "Continents"
print(our_string4[2:5])

# The character from the start to position 5
our_string5 = "Continents"
print(our_string5[:3])

# The character from the start to position 5
our_string6 = "Continents"
print(our_string6[:4])


# The character from the start to position 5
our_string7 = "Continents"
print(our_string7[:5])

print()


#? Negative Indexing

# The last element of a string can be accessed using the index -1.
our_string8 = "Continents"
print(our_string8[-1])

print()

# The second last element of a string can be accessed using the index -2.
my_string9 = "Best World!"
print(my_string9[-2])

print()


my_string10 = "Best World!"
print(my_string9[-5: -2])

