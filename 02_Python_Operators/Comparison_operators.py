
# Comparison operators are used to compare two values:
x = 5
y = 10


#? Equal to
# The double equals sign (==) is used to compare two values.
print(x == y)  # False

#? Not equal to
# The exclamation mark followed by an equal sign (!=) is used to compare two values.
print(x != y)  # True

#? Greater than
# The greater than operator (>) is used to check if the left operand is greater than the right operand.
print(x > y)  # False

#? Less than
# The less than operator (<) is used to check if the left operand is less than the right operand.
print(x < y)  # True

#? Greater than or equal to
# The greater than or equal to operator (>=) is used to check if the left operand is greater than or equal to the right operand.
print(x >= y)  # False

#? Less than or equal to
# The less than or equal to operator (<=) is used to check if the left operand is less than or equal to the right operand.
print(x <= y)  # True