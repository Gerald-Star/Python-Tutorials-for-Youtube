"""
Arithmetic operators are used to perform mathematical operations 
like addition, subtraction, multiplication, etc.
"""

# Arithmetic operators example
a = 10
b = 3

# Addition
result = a + b
print("Addition:", result)

# Subtraction
result = a - b
print("Subtraction:", result)

# Multiplication
result = a * b
print("Multiplication:", result)

# Division
result = a / b
print("Division:", result)

# Modulus
"""
Modulus operator (%) returns the remainder of the 
division of the first operand by the second operand.
"""
result = a % b
print("Modulus:", result)

# Exponentiation
"""
Exponentiation operator (**) raises the first operand 
to the power of the second operand.
"""
a = 2
b = 3
result = a ** b
print("Exponentiation:", result)

# Floor division
"""
  The floor division operator (//) returns 
  the integer part of the division result without the remainder.
  That is, it returns the quotient without the fractional part
"""
x = 10
y = 3
x // y  # Output: 3 (10 // 3 = 3)
print(x // y)  # Output: 3 (10 // 3 = 3)
print("Floor division:", x // y)